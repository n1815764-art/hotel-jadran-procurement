const TODAY = new Date("2026-04-18T00:00:00Z");

function daysOverdue(dueDate: string): number {
  const due = new Date(dueDate);
  const diff = Math.floor((TODAY.getTime() - due.getTime()) / (1000 * 60 * 60 * 24));
  return Math.max(0, diff);
}

